package chap_01;

public class _03_Variables {
    public static void main(String[] args) {
        String name = "나도코딩";
        int hour;
        hour    =15;


        System.out.println(name + "님, 배송이 시작 됩니다. "+ hour + "시에 방문 예정");
        System.out.println(name + "님, 배송이 완료");

        double score = 90.5;
        char grade = 'A';
        name = "강백호";
        System.out.println(name + "님의 점수는 " + score + "점 입니다.");
        System.out.println(name + "님의 등급은 " + grade + "입니다");

        boolean pass = true;
        System.out.println("이번시험에 합격 했을까요? " + pass);



    }
}
