package chap_02;

public class _02_Operator2 {
    public static void main(String[] args) {
        //복수 대입 연산
        int num = 10;
        System.out.println(num);
        System.out.println(num);

        //num = num + 2;
        num += 2;
        System.out.println(num);

        //num = num - 2;
        num -= 2;
        //num = num * 2;
        num *= 2;
        System.out.println(num);

        // 이렇게 생략해도 된다 그치만!! = 앞에 기호에 띄어쓰기를 해서는 안 된다




    }
}
